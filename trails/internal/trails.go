func registerTrailRoutes(api huma.API) {
	huma.Register(api, huma.Operation{
		OperationID: "list-trails",
		Method:      http.MethodGet,
		Path:        "/trail",
		Summary:     "List trails",
		Tags:        []string{"Trails"},
	}, func(ctx context.Context, _ *struct{}) (*TrailListOutput, error) {
		out := &TrailListOutput{}
		for name := range knownTrails {
			out.Body.Resources = append(out.Body.Resources, name)
		}
		return out, nil
	})

	huma.Register(api, huma.Operation{
		OperationID: "get-trail",
		Method:      http.MethodGet,
		Path:        "/trail/{name}",
		Summary:     "Get trail metadata",
		Tags:        []string{"Trails"},
	}, func(ctx context.Context, input *TrailInput) (*TrailOutputBody, error) {
		trail, ok := knownTrails[input.Name]
		if !ok {
			return nil, huma.Error404NotFound(fmt.Sprintf("trail %q not found", input.Name))
		}
		return &TrailOutputBody{
			Body: trail,
		}, nil
	})

	huma.Register(api, huma.Operation{
		OperationID: "get-trail-geometry",
		Method:      http.MethodGet,
		Path:        "/trail/{name}/geometry",
		Summary:     "Get trail line geometry",
		Tags:        []string{"Trails"},
	}, func(ctx context.Context, input *TrailInput) (*TrailGeometryOutput, error) {
		trail, ok := knownTrails[input.Name]
		if !ok {
			return nil, huma.Error404NotFound(fmt.Sprintf("trail %q not found", input.Name))
		}
		segments, ok := knownTrailGeometry[input.Name]
		if !ok {
			segments = []TrailSegment{{
				Coordinates: [][2]float64{
					{trail.Longitude - 0.005, trail.Latitude},
					{trail.Longitude + 0.005, trail.Latitude},
				},
				Color: "#4fc3f7",
				Style: TrailLineStyleSolid,
				Width: 3,
			}}
		}
		out := &TrailGeometryOutput{}
		out.Body.Segments = segments
		return out, nil
	})

	huma.Register(api, huma.Operation{
		OperationID: "get-trail-pois",
		Method:      http.MethodGet,
		Path:        "/trail/{name}/pois",
		Summary:     "Get points of interest for a trail",
		Tags:        []string{"Trails"},
	}, func(ctx context.Context, input *TrailInput) (*TrailPOIOutput, error) {
		if _, ok := knownTrails[input.Name]; !ok {
			return nil, huma.Error404NotFound(fmt.Sprintf("trail %q not found", input.Name))
		}
		out := &TrailPOIOutput{}
		out.Body.POIs = knownTrailPOIs[input.Name]
		if out.Body.POIs == nil {
			out.Body.POIs = []PointOfInterest{}
		}
		return out, nil
	})
}
