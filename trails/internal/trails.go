type TrailListOutput struct {
	Body struct {
		Resources []string `json:"resources" doc:"Names of available trails"`
	}
}

type TrailInput struct {
	Name string `path:"name" doc:"Trail name"`
}

type TrailOutputBody struct {
	Body TrailOutput
}

type TrailOutput struct {
	Name        string  `json:"name"`
	Description string  `json:"description"`
	LengthMiles float64 `json:"length_miles"`
	Difficulty  string  `json:"difficulty" enum:"easy,moderate,hard"`
	Latitude    float64 `json:"latitude"`
	Longitude   float64 `json:"longitude"`
}

type TrailLineStyle string

const (
	TrailLineStyleSolid  TrailLineStyle = "solid"
	TrailLineStyleDashed TrailLineStyle = "dashed"
	TrailLineStyleDotted TrailLineStyle = "dotted"
)

// TrailSegment is a contiguous run of [longitude, latitude] coordinates with uniform visual style.
type TrailSegment struct {
	Coordinates [][2]float64   `json:"coordinates"`
	Color       string         `json:"color"`
	Style       TrailLineStyle `json:"style" enum:"solid,dashed,dotted"`
	Width       float64        `json:"width"`
}

type TrailGeometryOutput struct {
	Body struct {
		Segments []TrailSegment `json:"segments"`
	}
}

type POIIconType string

const (
	POIIconTrailhead POIIconType = "trailhead"
	POIIconParking   POIIconType = "parking"
	POIIconCampsite  POIIconType = "campsite"
	POIIconViewpoint POIIconType = "viewpoint"
	POIIconWater     POIIconType = "water"
	POIIconRestroom  POIIconType = "restroom"
	POIIconPicnic    POIIconType = "picnic"
	POIIconSummit    POIIconType = "summit"
	POIIconJunction  POIIconType = "junction"
	POIIconHazard    POIIconType = "hazard"
	POIIconHistoric  POIIconType = "historic"
	POIIconGeneral   POIIconType = "general"
)

type PointOfInterest struct {
	Name        string      `json:"name"`
	Description string      `json:"description"`
	Latitude    float64     `json:"latitude"`
	Longitude   float64     `json:"longitude"`
	IconType    POIIconType `json:"icon_type" enum:"trailhead,parking,campsite,viewpoint,water,restroom,picnic,summit,junction,hazard,historic,general"`
	ImageURL    *string     `json:"image_url,omitempty"`
}

type TrailPOIOutput struct {
	Body struct {
		POIs []PointOfInterest `json:"pois"`
	}
}

